DATA SEGMENT  
    ARRAY DW 1,2,3,4,5
DATA ENDS

STACK SEGMENT STACK
STACK ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,SS:STACK,ES:EDATA
START:
    MOV AX,DATA
    MOV DS,AX
    LEA SI,ARRAY
    MOV CX,5
PRINTALL:
    PUSH CX 
    
    MOV BX,[SI]
    ADD SI,2
    
    MOV CX,16
OUTPUT:
    SHL BX,1
    MOV DL,0
    ADC DL,30H
    MOV AH,02H
    INT 21H
    LOOP OUTPUT
    
    MOV DL,13
    MOV AH,02H
    INT 21H
    MOV DL,10
    INT 21H
    
    POP CX
    LOOP PRINTALL
    
CODE ENDS
    END START